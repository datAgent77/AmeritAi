import { computeContractVersionHash, type PublishedContractVersion } from "@/lib/contracts"

export const PRIVACY_LANGUAGES = ["tr", "en", "de", "es"] as const

export type PrivacyLanguage = (typeof PRIVACY_LANGUAGES)[number]
export type PrivacyConsentEventType =
    | "notice_shown"
    | "notice_acknowledged"
    | "continued_after_notice"
    | "explicit_consent_granted"
    | "explicit_consent_rejected"
    | "withdrawn"

export type PrivacyConsentPurpose =
    | "basic_chat"
    | "chat_transcript_storage"
    | "lead_capture"
    | "appointment_request"
    | "marketing_contact"
    | "ai_processing"
    | "international_transfer"
    | "special_category_data"

export type PrivacyDocumentType =
    | "kvkkNotice"
    | "gdprPrivacyNotice"
    | "explicitConsentLead"
    | "explicitConsentAppointment"
    | "explicitConsentMarketing"
    | "explicitConsentSpecialCategory"
    | "explicitConsentInternationalTransfer"

export interface PrivacyComplianceDocument {
    type: PrivacyDocumentType
    title: string
    text: string
    language: PrivacyLanguage
    versionHash: string
    textHash: string
}

export interface PrivacyRequiredConsent {
    purpose: PrivacyConsentPurpose
    documentType: PrivacyDocumentType
    requiredBefore: "lead_submit" | "appointment_submit" | "marketing_opt_in" | "special_category_submit" | "international_transfer"
    checkboxLabelByLanguage: Record<PrivacyLanguage, string>
}

export interface PrivacyCompliancePayload {
    enabled: boolean
    mode: "hybrid"
    frameworks: Array<"kvkk" | "gdpr" | "ccpa" | "tcpa">
    language: PrivacyLanguage
    shortNoticeByLanguage: Record<PrivacyLanguage, string>
    documentsByLanguage: Record<PrivacyLanguage, Record<PrivacyDocumentType, PrivacyComplianceDocument>>
    requiredConsents: PrivacyRequiredConsent[]
    autoGeneratedFields: Record<string, string>
}

